# Условные операторы в Python

# Пример 1: Простое условие
x = -20

if x > 0:  
    # Этот блок выполнится, если x больше 0
    print('X - больше нуля.')

# Пример 2: Условие с блоком else
if x > 0:
    print('Х - больше нуля')
else:  
    # Этот блок выполнится, если предыдущее условие ложно
    print('X - не больше нуля')

# Пример 3: Использование elif (много вариантов)
x = -20

if x > 0:  
    print('Х - больше нуля')
elif x < 0:  
    print('Х - меньше нуля')  # Этот блок выполнится, так как x отрицательный
else:
    print('Х - равен нулю')

# Пример 4: Проверка совершеннолетия
age = 16
if age > 18:  
    print('Вы совершеннолетний')
else:
    print("Вы несовершеннолетний")

# Задача 1: Проверка числа на четность
number = 25689236598237645982763

# Проверяем, делится ли число на 2 без остатка
if number % 2 == 0:
    print("Число четное")
else:
    print("Число нечетное")

# Задача 2: Оценка по 5-балльной шкале
mark = 1000  # Пользователь вводит число от 2 до 5

if mark == 5:
    print("Отлично")
elif mark == 4:
    print("Хорошо")
elif mark == 3:
    print("Удовлетворительно")
elif mark == 2:
    print("Неудовлетворительно")
else:
    print("Некорректная оценка")  # Если введено число вне диапазона 2-5

# Задача 3: Определение времени суток
time = int(input("Введите время суток (0-23): "))

if 0 <= time < 6:
    print("Ночь")
elif 6 <= time < 12:
    print("Утро")
elif 12 <= time < 18:
    print("День")
elif 18 <= time < 24:
    print("Вечер")
else:
    print("Некорректное время")  # Если введено число вне диапазона 0-23
