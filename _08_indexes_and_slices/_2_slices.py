# Задача: вывести предпоследний символ строки, если длина больше 1
my_string = input("Введите строку: ")
if len(my_string) > 1:
    print(my_string[-2])  # Предпоследний символ

# Задача: проверить, делится ли первое число на второе без остатка
number1 = int(input("Введите первое число: "))
number2 = int(input("Введите второе число: "))
if number2 != 0:  # Предохранение от деления на 0
    if number1 % number2 == 0:
        print("Делится")
    else:
        print("Не делится")
else:
    print("На ноль делить нельзя!")

