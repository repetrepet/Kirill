# Задача 1: Определение четного или нечетного числа
# Запрашиваем у пользователя число и определяем, является ли оно четным или нечетным.
number = int(input("Введите число: "))
if number % 2 == 0:
    print("Число четное")  # Вывод, если число четное
else:
    print("Число нечетное")  # Вывод, если число нечетное

# Задача 2: Определение возраста
# Запрашиваем у пользователя его возраст и выводим, является ли он совершеннолетним (18 лет и старше) или несовершеннолетним.
age = int(input("Введите ваш возраст: "))
if age >= 18:
    print("Вы совершеннолетний")  # Вывод, если возраст 18 и старше
else:
    print("Вы несовершеннолетний")  # Вывод, если возраст меньше 18

# Задача 3: Оценка по баллам
# Запрашиваем у пользователя количество баллов (от 0 до 100) и выводим соответствующую оценку.
mark = int(input("Введите ваш балл: "))

if 90 <= mark <= 100:
    print("Отлично")  # Вывод, если балл от 90 до 100
elif 75 <= mark <= 89:
    print("Хорошо")  # Вывод, если балл от 75 до 89
elif 60 <= mark <= 74:
    print("Удовлетворительно")  # Вывод, если балл от 60 до 74
elif 0 <= mark <= 59:
    print("Неудовлетворительно")  # Вывод, если балл от 0 до 59
else:
    print("Некорректный балл")  # Вывод, если балл вне диапазона

# Задача 4: Проверка на високосный год
# Запрашиваем у пользователя год и определяем, является ли он високосным.
year = int(input("Введите год: "))
if year % 4 == 0:
    print("Год високосный")  # Вывод, если год делится на 4
else:
    print("Год не високосный")  # Вывод, если год не делится на 4

# Калькулятор:
# Запрашиваем у пользователя два числа и оператор (например, +, -, *, /), и программа выводит результат операции.
number1 = int(input("Введите первое число: "))  # Запрашиваем первое число
number2 = int(input("Введите второе число: "))  # Запрашиваем второе число
operator = input("Введите оператор (+, -, *, /): ")  # Запрашиваем оператор

# Проверяем, какой оператор ввел пользователь и выполняем соответствующую операцию
if operator == "+":
    print(number1 + number2)  # Выводим сумму двух чисел
elif operator == "-":
    print(number1 - number2)  # Выводим разность двух чисел
elif operator == "*":
    print(number1 * number2)  # Выводим произведение двух чисел
elif operator == "/":
    if number2 != 0:  # Проверяем, не равно ли второе число нулю
        print(number1 / number2)  # Выводим результат деления
    else:
        print('Деление на ноль невозможно!')  # Сообщаем об ошибке деления на ноль
else:
    print("Некорректный оператор")  # Сообщаем, если оператор не распознан

